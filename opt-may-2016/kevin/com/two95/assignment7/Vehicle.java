package com.two95.assignment7;

public class Vehicle {
  String model;
  int make;
public Vehicle(String model, int make) {
	super();
	this.model = model;
	this.make = make;
}
public String getModel() {
	return model;
}
public void setModel(String model) {
	this.model = model;
}
public int getMake() {
	return make;
}
public void setMake(int make) {
	this.make = make;
}
  
  
}
